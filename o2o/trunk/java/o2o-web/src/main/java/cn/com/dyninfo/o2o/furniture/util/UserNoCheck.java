package cn.com.dyninfo.o2o.furniture.util;
import java.util.regex.Pattern;

public class UserNoCheck {

	public static final String passW="_m_uy";
	public static boolean isPhone(String context){
		return Pattern.matches("1[\\d]{10}", context);
	}
	
	public static boolean isEmail(String context){
		return Pattern.matches("([a-zA-Z0-9]+[_|\\_|\\.]?)*[a-zA-Z0-9]+@([a-zA-Z0-9]+[_|\\_|\\.]?)*[a-zA-Z0-9]+\\.[a-zA-Z]{2,3}", context);
	}
}
